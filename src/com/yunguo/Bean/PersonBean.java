package com.yunguo.Bean;

public class PersonBean {
	private String Name;
	private String IDcard;
	private String Sex;
	private String Nation;
	private String Adress;
	private String Datatimebegin;
	private String DatatimeEnd;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getIDcard() {
		return IDcard;
	}
	public void setIDcard(String iDcard) {
		IDcard = iDcard;
	}
	public String getSex() {
		return Sex;
	}
	public void setSex(String sex) {
		Sex = sex;
	}
	public String getNation() {
		return Nation;
	}
	public void setNation(String nation) {
		Nation = nation;
	}
	public String getAdress() {
		return Adress;
	}
	public void setAdress(String adress) {
		Adress = adress;
	}
	public String getDatatimebegin() {
		return Datatimebegin;
	}
	public void setDatatimebegin(String datatimebegin) {
		Datatimebegin = datatimebegin;
	}
	public String getDatatimeEnd() {
		return DatatimeEnd;
	}
	public void setDatatimeEnd(String datatimeEnd) {
		DatatimeEnd = datatimeEnd;
	}
	
}
